async function main() {
    console.log('Adding blog post: ' + blogPost.title)

    try {
        // 1. Check if category exists or create it
        let category = await prisma.category.findUnique({
            where: { slug: blogPost.categorySlug }
        })

        if (!category) {
            console.log(`Category '${blogPost.categorySlug}' not found. Creating it...`)
            category = await prisma.category.create({
                data: {
                    name: "AI Tools",
                    slug: blogPost.categorySlug,
                    description: "Discover the best AI tools and software to boost productivity.",
                    icon: "🚀"
                }
            })
            console.log('Category created with ID:', category.id)
        } else {
            console.log('Found existing category ID:', category.id)
        }

        // 2. Check if blog post exists
        const existingPost = await prisma.blogPost.findUnique({
            where: { slug: blogPost.slug }
        })

        if (existingPost) {
            console.log(`Blog post '${blogPost.slug}' already exists. Updating...`)
            await prisma.blogPost.update({
                where: { slug: blogPost.slug },
                data: {
                    title: blogPost.title,
                    excerpt: blogPost.excerpt,
                    content: blogPost.content,
                    metaTitle: blogPost.metaTitle,
                    metaDescription: blogPost.metaDescription,
                    focusKeyword: blogPost.focusKeyword,
                    coverImage: blogPost.imageUrl,
                    categoryId: category.id,
                    published: true,
                    publishedAt: new Date(),
                    featured: true
                }
            })
            console.log('Blog post updated successfully.')
        } else {
            console.log(`Creating new blog post...`)
            await prisma.blogPost.create({
                data: {
                    title: blogPost.title,
                    slug: blogPost.slug,
                    excerpt: blogPost.excerpt,
                    content: blogPost.content,
                    metaTitle: blogPost.metaTitle,
                    metaDescription: blogPost.metaDescription,
                    focusKeyword: blogPost.focusKeyword,
                    coverImage: blogPost.imageUrl,
                    categoryId: category.id,
                    published: true,
                    publishedAt: new Date(),
                    featured: true,
                    views: 450 // Start with some social proof views
                }
            })
            console.log('Blog post created successfully.')
        }

    } catch (error) {
        console.error('Error adding blog post:', error)
    } finally {
        await prisma.$disconnect()
    }
}

main()
