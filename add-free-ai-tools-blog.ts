async function main() {
    console.log('Adding blog post: ' + blogPost.title)

    try {
        // 1. Check if category exists or create it
        let category = await prisma.category.findUnique({
            where: { slug: blogPost.categorySlug }
        })

        if (!category) {
            console.log('Creating category: ' + blogPost.categorySlug)
            category = await prisma.category.create({
                data: {
                    name: 'AI Tools',
                    slug: blogPost.categorySlug,
                    description: 'Comprehensive guides, reviews, and comparisons of the best AI tools for productivity, creativity, and business.'
                }
            })
        }

        // 2. Create or Update the blog post
        const result = await prisma.blogPost.upsert({
            where: { slug: blogPost.slug },
            update: {
                title: blogPost.title,
                metaTitle: blogPost.metaTitle,
                metaDescription: blogPost.metaDescription,
                focusKeyword: blogPost.focusKeyword,
                excerpt: blogPost.excerpt,
                content: blogPost.content,
                published: true,
                category: {
                    connect: { id: category.id }
                },
                coverImage: blogPost.imageUrl
            },
            create: {
                title: blogPost.title,
                slug: blogPost.slug,
                metaTitle: blogPost.metaTitle,
                metaDescription: blogPost.metaDescription,
                focusKeyword: blogPost.focusKeyword,
                excerpt: blogPost.excerpt,
                content: blogPost.content,
                published: true,
                category: {
                    connect: { id: category.id }
                },
                coverImage: blogPost.imageUrl,
                publishedAt: new Date()
            }
        })

        console.log('âœ… Successfully published: ' + result.title)

    } catch (error) {
        console.error('âŒ Failed:', (error as Error).message)
    }

    console.log('\n\nScript complete!')
}

